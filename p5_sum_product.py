# Description: Write a program which computes the sum and product of two numbers
# entered by the user.

#difference of two numbers:

#INPUT: ask the user to enter two numbers
num1 = int ( input ( "please enter first number:" ) )
num2 = int ( input ( "please enter second number:" ) )

# PROCESSING: calculate the sum
sum = num1 + num2

# OUTPUT: show the result
print (num1, "+", num2, "=", sum)

# PROCESSING: calculate the product
product = num1 * num2

# OUTPUT: show the result
print (num1, "*", num2, "=", product)


